										巿長林智堅今(26)日進行現勘，期打造「新竹1916園區」成為具國際觀光亮點園區。 
										林智堅巿長下午現勘「麗池親水親樹景觀空間營造計畫」工程，
										麗池累積近十年淤泥異味終獲改善，
										會中樂駝規畫設計公司環池說明近日執行清淤狀況，
