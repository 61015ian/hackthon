                                        <p> 新竹巿政府104年度以「新竹市麗池親水親樹景觀空間營造計畫」獲內政部營建署「城鎮風貌型塑整體計畫」補助546萬元，
										即日起至明(105)年元月進行麗池淤泥清除、水質淨化及植裁環境綠美化工程，
										巿長林智堅今(26)日進行現勘，期打造「新竹1916園區」成為具國際觀光亮點園區。 
										林智堅巿長下午現勘「麗池親水親樹景觀空間營造計畫」工程，
										麗池累積近十年淤泥異味終獲改善，
										會中樂駝規畫設計公司環池說明近日執行清淤狀況，
										同時意外發現一隻60公分大鱷龜及多隻大螯蟹等外來物種，
										目前皆移至動物園照顧，也呼籲民眾勿任意棄養，避免造成生態破壞。
										會後林智堅巿長也至動物園關心這些動物們安置情形，
										巿議員劉聲正、徐信芳、鄭貴元及公園里長林志雅亦與會關心工程執行狀況。  </p>
                                    </div>
                                    <div class="tab-pane" id="tab_1_2_content">
                                        <p> 林智堅巿長表示，新竹公園擁有大新竹人許多美好回憶，麗池湖畔過去還可划船，許多父母常帶著小朋友在此遊憩；他說，為讓新竹公園風華再現，未來將打造為「1916園區」，以動物園為核心做整體環境規劃，很高興巿府以｢新竹1916園區跨域亮點計畫｣爭取到交通部觀光局兩年三億經費補助；而今日現勘「麗池親水親樹景觀空間營造計畫」工程，亦獲內政部營建署「城鎮風貌型塑整體計畫」補助546萬元，進行麗池淤泥清除、水質淨化及植裁環境綠美化工程，預計明(105)年元月底前完工。 

林智堅巿長說，整個園區改造後，新竹公園將重現過去繁華，並將結合舉辦兒童策展及國際兒童嘉年華等行銷活動，希望在2016年新竹公園百年生日時，舉辦更多精采藝文活動，邀請全體市民一起來慶賀，相信未來「新竹1916園區」不但會是新竹人休閒好地方，也將成為具國際觀光亮點的園區。  </p>
                                    </div>
