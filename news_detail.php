										期藉此徹底消除近十年累積淤泥產生之異味與優養化問題，也可提供親水、調節氣候、減低都市熱島效應，
										全新風貌將讓漫步公園民眾享有清新自然好環境。 
										文化局指出，
										為整體規劃1916園區，
										巿府以｢新竹1916園區跨域亮點計畫｣爭取到交通部觀光局三億經費，
										將做為整合遊憩資源、提昇旅遊環境、行銷主題式觀光亮點及振興地方經濟發展之用，
										期將新竹市打造成具深度、吸引力之觀光城市；
										園區未來將以麗池生態水岸串連周邊日式湖畔料亭、玻工館、願景館、九曲橋，
										並隨著四季風貌、節日慶典呈現城市多樣丰采，期以軟體活動帶出硬體設施新價值，
										讓1916園區展現新竹榮耀。 </p>


